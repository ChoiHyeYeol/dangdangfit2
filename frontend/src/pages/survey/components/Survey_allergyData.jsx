// (선택) 항목 표준화/검색용 헬퍼
export const buildAllergyIndex = (cats = allergyCategories) =>
  cats.flatMap((c) =>
    c.items.map((name) => ({
      name,
      norm: normalize(name),
      catKey: c.key,
      catLabel: c.label,
    }))
  );

export const normalize = (s = "") => s.toLowerCase().replace(/\s+/g, "");

